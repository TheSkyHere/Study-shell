#!/bin/sh



pwm=$(( ( $1 * 255 ) / 100 ))
pwm=$(printf "%X\n" $pwm)
echo "$pwm"

i2ctransfer -f -y 14 w3@0x20 0x04 0x21 0x$pwm
i2ctransfer -f -y 14 w3@0x20 0x04 0x22 0x$pwm

i2ctransfer -f -y 14 w3@0x20 0x04 0x25 0x$pwm
i2ctransfer -f -y 14 w3@0x20 0x04 0x26 0x$pwm

i2ctransfer -f -y 14 w3@0x20 0x04 0x29 0x$pwm
i2ctransfer -f -y 14 w3@0x20 0x04 0x2a 0x$pwm

i2ctransfer -f -y 14 w3@0x20 0x04 0x2d 0x$pwm
i2ctransfer -f -y 14 w3@0x20 0x04 0x2e 0x$pwm

i2ctransfer -f -y 14 w3@0x20 0x04 0x31 0x$pwm
i2ctransfer -f -y 14 w3@0x20 0x04 0x32 0x$pwm